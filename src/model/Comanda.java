public class Comanda {
    private int id;
    private String daCasa;
    private String burguer;
    private String kids;
    private String bacon;
    private String cheddar; 
    private String aguaGas;
    private String agua;
    private String cocaCola;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDaCasa() {
        return daCasa;
    }

    public void setDaCasa(String daCasa) {
        this.daCasa = daCasa;
    }

    public String getBurguer() {
        return burguer;
    }

    public void setBurguer(String burguer) {
        this.burguer = burguer;
    }

    public String getKids() {
        return kids;
    }

    public void setKids(String kids) {
        this.kids = kids;
    }

    public String getBacon() {
        return bacon;
    }

    public void setBacon(String bacon) {
        this.bacon = bacon;
    }

    public String getCheddar() {
        return cheddar;
    }

    public void setCheddar(String cheddar) {
        this.cheddar = cheddar;
    }

    public String getAguaGas() {
        return aguaGas;
    }

    public void setAguaGas(String aguaGas) {
        this.aguaGas = aguaGas;
    }

    public String getAgua() {
        return agua;
    }

    public void setAgua(String agua) {
        this.agua = agua;
    }

    public String getCocaCola() {
        return cocaCola;
    }

    public void setCocaCola(String cocaCola) {
        this.cocaCola = cocaCola;
    }

    
}
